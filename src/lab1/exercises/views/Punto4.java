    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        lblError.setText("");
        try {
            String ordenId = txtOrdenId.getText();
            orders.removeIf(o -> o.getId().equalsIgnoreCase(ordenId));
            completeData();
        } catch (IOException e) {
            lblError.setText("Ocurrió un error");
            Logger.getLogger(Punto4.class.getName()).log(Level.SEVERE, null, e);
        } finally {
            txtProcessTime.setText("");
            txtDeliveryTime.setText("");
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

