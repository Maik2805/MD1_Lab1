    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            lblError.setText("");
            String ordenId = txtOrdenId.getText();
            float processTime = Float.parseFloat(txtProcessTime.getText());
            int delvieryTime = Integer.parseInt(txtDeliveryTime.getText());

            orders.add(new Order(ordenId, processTime, delvieryTime));
            completeData();
        } catch (Exception e) {
            lblError.setText("Ocurrió un error");
            Logger.getLogger(Punto4.class.getName()).log(Level.SEVERE, null, e);
        } finally {
            txtOrdenId.setText("");
            txtProcessTime.setText("");
            txtDeliveryTime.setText("");
        }
    }//GEN-LAST:event_btnAddActionPerformed

