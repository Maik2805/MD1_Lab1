    private void completeData() throws IOException {

        balance = orderController.generatePepsBalance(orders);
        tableModel.setOrders(balance.getOrders());
        OrderController.showBalance(balance);
        tableModel.fireTableDataChanged();
        txtUtilization.setText(String.valueOf(balance.getUtilization()));
        txtAverageTimeTermination.setText(String.valueOf(balance.getAverageTimeTermination()));
        txtSystemJobs.setText(String.valueOf(balance.getSystemJobs()));
        txtAverageDelay.setText(String.valueOf(balance.getAverageDelay()));
    }

